package agentSim.cosmos;

public class Coordinates {
    private int axisX;
    private int axisY;

    public Coordinates(int axisX, int axisY) {
        this.axisX = axisX;
        this.axisY = axisY;
    }

    public int getAxisX() {
        return axisX;
    }

    public int getAxisY() {
        return axisY;
    }
}
